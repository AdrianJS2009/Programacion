/**
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for
 * 
 * @author Adrián Jiménez Santiago
 */
public class T05_Ejercicio01 {

  public static void main(String[] args) {
    for (int i = 0; i <= 100; i++) {
      int resultado = i + 5;

      System.out.println(resultado);
    }
  }
}